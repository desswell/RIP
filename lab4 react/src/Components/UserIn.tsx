import React from 'react'
import {DelActiveAction, SetActiveAction, useUsers} from "../slices/userSlice";
import {useDispatch} from "react-redux";

export function BtnLogIn(){
    const state = useUsers()
    const dispatch = useDispatch()
    const log = state ? 'Выход' : 'Вход'
    return(
    <button onClick={() => state ? dispatch(DelActiveAction()) : dispatch(SetActiveAction())}>
        {log}
    </button>
    )
}